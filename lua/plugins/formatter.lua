-- Name: Formatter
-- Description: Linter/formatter invoker
-- Link: https://github.com/mhartington/formatter.nvim

local formatter = require("formatter")

local remap = vim.api.nvim_set_keymap

------------------------------------------------------------------------------------------
----------------------------------- SETUP ------------------------------------------------
------------------------------------------------------------------------------------------

formatter.setup(
    {
        filetype = {
            lua = {},
        },
    }
)

------------------------------------------------------------------------------------------
----------------------------------- REMAPS -----------------------------------------------
------------------------------------------------------------------------------------------

-- Format code
remap(
    "n", "<leader>ff",
    ":FormatWrite<CR>",
    { silent = true, noremap = true }
)
