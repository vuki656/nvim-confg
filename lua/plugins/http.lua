-- Name: Kulala
-- Description: Run HTTP requests from nvim
-- Link: https://github.com/mistweaverco/kulala.nvim

local kulala = require("kulala")
local set_keymap = require("utils.set-keymap")

------------------------------------------------------------------------------------------
----------------------------------- SETUP ------------------------------------------------
------------------------------------------------------------------------------------------

kulala.setup()

------------------------------------------------------------------------------------------
----------------------------------- KEYMAPS ----------------------------------------------
------------------------------------------------------------------------------------------

set_keymap({
    key = "<LEADER>kur",
    actions = function()
        kulala.run()
    end,
    description = "Run HTTP file",
})
