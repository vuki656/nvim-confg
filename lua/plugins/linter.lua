-- Name: Nvim Lint
-- Description: Linter
-- Link: https://github.com/mfussenegger/nvim-lint

local lint = require("lint")

------------------------------------------------------------------------------------------
----------------------------------- SETUP ------------------------------------------------
------------------------------------------------------------------------------------------

vim.api.nvim_create_autocmd({ "BufWritePost", "BufEnter" }, {
    callback = function()
        lint.try_lint()
    end,
})

------------------------------------------------------------------------------------------
----------------------------------- MAPPING ----------------------------------------------
------------------------------------------------------------------------------------------

lint.linters_by_ft = {
    lua = {
        "luacheck",
    },
    dockerfile = {
        "hadolint",
    },
    sh = {
        "shellcheck",
    },
    scss = {
        "stylelint",
    },
    less = {
        "stylelint",
    },
    css = {
        "stylelint",
    },
    sass = {
        "stylelint",
    },
    yaml = {
        "yamllint",
        "actionlint",
    },
}

------------------------------------------------------------------------------------------
----------------------------------- OVERRIDES --------------------------------------------
------------------------------------------------------------------------------------------

lint.linters.yamllint.args = {
    "--format",
    "parsable",
    "-d",
    [[{
        rules: {
            line-length: { max: 140 },
            document-start: { present: false }
        }
    }]],
    "-",
}
